////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
if (num1 < num2) {
  return num1;
} else if (num2 < num1) {
  return num2;
} else if (num1 === num2) {
  return num1;
}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
if (n < 0) {
  return isEven(-n)
} else {
  if (n === 0) {
    return true
  } else if (n === 1) {
    return false
  } else {
    n -= 2;
    return isEven(n)
  }
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
let count = 0;
  if (char === undefined) {
    return 'Must Enter Character to Count'
  } else {
    for (let i = 0; i < str.length; i++) {
    if (str[i] === char) {
      count++
    }
  }
}
return count
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  let count = 0;
  if (str === undefined) {
    return 'Must Enter Word'
  } else {
    for (let i = 0; i < str.length; i++) {
    if (str[i] === 'B') {
      count++
    }
  }
}
return count
}

